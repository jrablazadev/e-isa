			{
				var $iframe = $(elem).length ? $(elem) : window.parent.$(elem);
				var padding = 10;

				$iframe.css( 'height', '0px' );
				var height = $iframe.get(0).contentWindow.document.body.scrollHeight + padding;
				$iframe.css( 'height', (height + 20) + 'px' );
